/* USER CODE END Header_UsbTask */
void UsbTask(void const * argument)
{
  /* USER CODE BEGIN UsbTask */
	uint16_t i,j,k;
	uint8_t a = 0;
	uint32_t value;
	osEvent event;
  /* Infinite loop */
  for(;;)
  {
		event = osMessageGet(QueueTxHandle, 0xffffffff);
		if(event.status == osEventMessage)
		{
			value = event.value.v;
			j = 0, k = 0;
			
			for(i=0; i<ad7768_device.data_block_size; i++)
			{
				data_send[j++] = sai_data[value + i];
				if(++a == 4)
				{
					data_send[j++] = digital_clk[k++];
					a = 0;
				}
//				if(i<digital_cnt)
//				{
//					sai_data[value+i+ad7768_device.data_block_size] = digital_clk[i] = 0;[i];
//				}
//				else
//				{
//					sai_data[value+i+ad7768_device.data_block_size] = 0;
//				}
			}
			
			digital_cnt = 0;
			for(i=0; i<256; i++)
			{
				digital_clk[i] = 0;
			}
			
			CDC_Transmit_HS((uint8_t *)data_send, ad7768_device.data_block_size*5);
			
		}
  }
  /* USER CODE END UsbTask */
}

